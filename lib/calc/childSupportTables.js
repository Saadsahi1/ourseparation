// Courts always apply the tables in force at the time of the order/calculation,
// not the tables in force at the date of separation.
// We keep the separationDate param for legacy signature compatibility but ignore it.
export function selectCSTByDate(_separationDate) {
  const today = new Date()
  for (const r of CST_RANGES) {
    if (today >= r.start && today <= r.end) return r.name
  }
  return '2025 CST'
}

// ─── Lookup ───────────────────────────────────────────────────────────────────
export function lookupChildSupport(tableName, income, numChildren) {
  const col  = Math.min(Math.max(1, numChildren), 6)  // 1-indexed col = children count
  const use2025 = tableName === '2025 CST'
  const table = use2025 ? CST_2025 : CST_2017
  const over  = use2025 ? CST_2025_OVER : CST_2017_OVER
  const startIncome = table[0][0]

  if (income <= startIncome) return 0

  const maxTableIncome = 150000
  if (income > maxTableIncome) {
    // Use formula: base + rate * (income - 150000)
    return Math.round(over.base[col - 1] + over.rate[col - 1] * (income - maxTableIncome))
  }

  // Floor to nearest $1,000 and interpolate linearly to next $1,000
  const lowerIncome = Math.floor(income / 1000) * 1000
  const idx = Math.round((lowerIncome - startIncome) / 1000)

  if (idx >= table.length - 1) return table[table.length - 1][col]

  const lower = table[idx]
  const upper = table[idx + 1]
  const frac  = (income - lower[0]) / 1000
  return Math.round(lower[col] + frac * (upper[col] - lower[col]))
}
